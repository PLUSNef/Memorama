    public void evaluar(){
        if (tiro==2){
            if(imagenes[pos1].equals(imagenes[pos2])){//tarjetas iguales
                if(turnoJ1){
                    pares1++;
                    lblCount1.setText(""+pares1);
                } else {
                  pares2++;
                  lblCount2.setText(""+pares2);
                }
            } else {
                switch(listCat.getSelectedIndex()){ 
                    case 0:
                        break;
                    case 1:
                        tarjetas[pos1].setIcon(new ImageIcon(getClass().getResource("/images/cardkw.jpg")));
                        tarjetas[pos2].setIcon(new ImageIcon(getClass().getResource("/images/cardkw.jpg")));
                        break;
                    case 2:
                        tarjetas[pos1].setIcon(new ImageIcon(getClass().getResource("/images/calavera.jpg")));
                        tarjetas[pos2].setIcon(new ImageIcon(getClass().getResource("/images/calavera.jpg")));
                }
              volteadas[pos1]=false;
              volteadas[pos2]=false;
              
              turnoJ1 = !turnoJ1;
              if(turnoJ1){
                  lblJ1.setForeground(new java.awt.Color(255,0,0));// cambia el color al jugador 1 que es quien empieza
                  lblJ2.setForeground(new java.awt.Color(0,0,0));
              } else {
                  lblJ1.setForeground(new java.awt.Color(0,0,0));
                  lblJ2.setForeground(new java.awt.Color(255,0,0));// cambia el color al jugador 2 que es quien empieza
              }
            }
          tiro = 0;
        }
      
      if ((pares1+pares2)==12) { // la condicion revisa que todas las tarjetas esten en true (volteadas)
          if(pares1>pares2){ //esta segunda condicion es para ver su el primer jugador es el ganador 
              JOptionPane.showMessageDialog(null, "You are Winner ^w^r : " + txtJugador1.getText()); //Manda un mensaje del ganador
              
              
      
    
              
          } else if (pares2 > pares1) { //en caso de que no entonces evaluara si el segundo jugador entonces es el ganadpr
              JOptionPane.showMessageDialog(null, "You are Winner ^w^r : " + txtJugador2.getText());
              
            
              
            } else { //en caso de que ambos tengan el mismo numero de pares sera declarado un empate
                JOptionPane.showMessageDialog(null, "Empate :v"); //en caso de empate me manda el mensaje
                }
          pares1 = 0; //me inicializa la variable del pares1 en 0
          pares2 = 0; //me inicializa la variable del pares2 en 0
          
          JOptionPane.showMessageDialog(null, "Juego Terminado"); 
            int YesOrNot = JOptionPane.showConfirmDialog(null, "JUEGO TERMINADO ¿Desea seguir juagndo?", "Salir", JOptionPane.YES_NO_OPTION);//me da la opcion de si deseo seguir jugando
            if (YesOrNot == 0) { //en caso de que si
            
                JOptionPane.showMessageDialog(null, "A JUGAR!!"); // me manda el mensaje
                this.setVisible(false); 
                Memorama.main(null);// me carga el juego de nuevo
                return;
                
            } else {
                System.exit(0); //en caso de que no, se sale y lo cierra
            }
          
          }
      
        
    }
    
