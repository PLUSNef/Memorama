    public void mezclar(){ // metodo que me permite mezclar las imagenes con la clase random
        Random r=new Random();
        int x;
        int y;
        String tempo; //declaramos una variable tempo 
        for (int i = 1; i <=500; i++){ // el for me permite revolver las imagenes hasta 500 veces
            x = r.nextInt(24); // pone el en uso la variable r de random() hasta un tope que es 24
            y = r.nextInt(24);
            tempo = imagenes[x]; //la variable tempo almacenara a la variable x (la imagen)
            imagenes[x] = imagenes[y]; // la imagen x ahora se cambiara a donde esta la imagen y
            imagenes[y] = tempo; // para que no se pierda y, la variable tempo almacenara el valor de y que ahora es x
        }
    }
    
    public void tirar(int pos){
        
        if (volteadas[pos]==false){//tarjeta disponible
            volteadas[pos]=true;// marca que la tarjeta ya no esta disponibler ya que ha sido volteada
            tarjetas[pos].setIcon(new ImageIcon(getClass().getResource(imagenes[pos]))); 
            if(tiro==0){ //si la variable tiro es igual a 0 entonces
                pos1=pos; //me indica que la variable pos1 sera igual a la posicion de la tarjeta volteada
                
            }else{ // en caso de ser falso
                pos2=pos; //me indica que ña variable pos2 toma el valor de la posicion de la segunda tarjeta volteada
                
            }
            tiro++; //incrementa la variable tiro
            // me manda el audio cuando doy clic a alguna tarjeta
            try {
            AePlayWave aw;
            URL url = PlayAudio.class.getResource("/audios/end.wav");
            aw = new AePlayWave(url);
            aw.start();
        } catch (URISyntaxException ex) {
            Logger.getLogger(PlayAudio.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
        
        
        
    }
    
